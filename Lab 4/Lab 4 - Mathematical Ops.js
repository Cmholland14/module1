function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;

}

function divide(a, b) {
    return a / b;
}

console.log(add(5,5)); //10
console.log(subtract(9,3)); //6
console.log(multiply(6,3)); // 18
console.log(divide(12,4)); //3